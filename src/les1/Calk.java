package les1;

public class Calk {

    public double mul(int a, int b){
        return a * b;
    }
    public double sum(int a, int b){
        return a + b;
    }
    public double div(int a, int b){
        if (a != 0)
            return a / b;
        return 0;
    }
    public double sub(int a, int b){
        return a - b;
    }
}
